package operadores;

public class operates {
    public static void main(String[] args) {
        // Arithmetics +, -, *, /
        // Relational ==, >, < >=, <=
        // Conditional &&, ||, !
        int num1, num2, resultArithmetics;
        num1=3;
        num2 = 2;
        resultArithmetics = num1 + num2;
        System.out.println(resultArithmetics);
        resultArithmetics = num1 - num2;
        System.out.println(resultArithmetics);
        resultArithmetics = num1 * num2;
        System.out.println(resultArithmetics);
        resultArithmetics = num1 / num2;
        System.out.println(resultArithmetics);
        System.out.println(resultArithmetics);
        System.out.println(resultArithmetics);
    }
}